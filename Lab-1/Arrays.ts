// Array of Numbers

let arr : Number[] = [1,2,3,4,5];
console.log("Array of Numbers:", arr);

// Array of Strings

let fruits : String[] = ["Apple", "Banana", "Orange"];
console.log("Array of Strings:", fruits);

// Mixed Array

let mixedArr : (number | string | boolean)[] = [1, "Two", true, 4, "Five", false];
console.log("Mixed Array:", mixedArr);

// Array of Objects

let users : { id: number; name: string }[] = [
  { id: 1, name: "Kavya" },
  { id: 2, name: "Samarya" },
];
console.log("Array of Objects:", users);

// Multi-dimensional Array

let matrix : number[][] = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9],
];
console.log("Multi-dimensional Array:", matrix);